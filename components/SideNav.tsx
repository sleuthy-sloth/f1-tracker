export function SideNav() {
  const pathname = usePathname();

  const isActive = (href: string) => {
    return pathname === href || pathname.startsWith(href + "/");
  };

  return (
    <nav
      className="hidden md:flex fixed left-0 top-0 h-screen w-60 flex-col border-r border-white/5 bg-surface"
      aria-label="Main navigation"
    >
      {/* Branding */}
      <div className="flex h-16 items-center border-b border-white/5 px-4">
        <span className="font-heading text-xl font-bold tracking-tight text-f1-white">
          SECTOR
          <span className="text-f1-red">ONE</span>
        </span>
      </div>

      {/* Navigation Items */}
      <div className="flex flex-1 flex-col gap-1 p-4">
        {NAV_ITEMS.map((item) => {
          const Icon = item.icon;
          const active = isActive(item.href);

          return (
            <Link
              key={item.href}
              href={item.href}
              className={`flex items-center gap-3 rounded-md px-3 py-2.5 font-sans text-sm transition-colors ${
                active
                  ? "border-l-2 border-f1-red bg-f1-red/5 text-f1-red"
                  : "border-l-2 border-transparent text-f1-silver hover:bg-white/5 hover:text-f1-white"
              }`}
              aria-current={active ? "page" : undefined}
            >
              <Icon />
              <span className="font-medium">{item.label}</span>
            </Link>
          );
        })}
      </div>

      {/* Version */}
      <div className="border-t border-white/5 p-4">
        <span className="text-xs text-f1-silver/50">v0.1.0</span>
      </div>
    </nav>
  );
}